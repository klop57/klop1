# 다음과 같이 리스트를 만들고
# 반복문으로 요소를 하나씩 출력하세요
lis = ['a','b','c','d','e']

print(lis[0])
print(lis[1])
print(lis[2])
print(lis[3])
print(lis[4])
# 반복횟수 : 5
# 반복코드 : print(lis[?])

